# capitalize() 将第一个字母大写
'''
capitalize(...)
    S.capitalize() -> str

    Return a capitalized version of S, i.e. make the first character
    have upper case and the rest lower case.
'''
#print(help(str.capitalize))
a = 'asdffghjkl'
b = a.capitalize()
print(b)                            # b = Asdffghjkl
